package org.janitor.tetris.ui;

/**
 * The position defines a pixel position on a paintable panel.
 */
public class Position {
    public int x;
    public int y;

    /**
     * Constructor.
     * @param x The x axis
     * @param y The y axis
     */
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
